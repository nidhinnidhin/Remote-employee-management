import { Injectable, Inject, ForbiddenException } from '@nestjs/common';
import { isValidObjectId } from 'mongoose';
import type { UserRepository } from 'src/modules/auth/domain/repositories/user.repository';
import type { CompanyRepository } from 'src/modules/auth/domain/repositories/company.repository';
import { UserStatus } from 'src/shared/enums/user/user-status.enum';
import { UserRole } from 'src/shared/enums/user/user-role.enum';
import { CompanyStatus } from 'src/shared/enums/company/company-status.enum';
import { JwtService } from 'src/shared/services/jwt.service';
import { UserEntity } from 'src/modules/auth/domain/entities/user.entity';
import { AUTH_MESSAGES } from 'src/shared/constants/messages/auth/auth.messages';
import {
  SocialLoginInput,
  SocialLoginResponse,
} from 'src/shared/types/auth/social-login.type';

@Injectable()
export class SocialLoginUseCase {
  constructor(
    @Inject('UserRepository')
    private readonly _userRepository: UserRepository,
    @Inject('CompanyRepository')
    private readonly _companyRepository: CompanyRepository,
    private readonly _jwtService: JwtService,
  ) { }

  async execute(input: SocialLoginInput): Promise<SocialLoginResponse> {
    const { email, firstName, lastName, provider, providerId } = input;
    let user = await this._userRepository.findByEmail(email.toLowerCase());

    console.log('-----------------------------------------');
    console.log(' SOCIAL LOGIN/REGISTER USE CASE');
    console.log(' Email:', email);
    console.log(' User Found:', !!user);

    if (!user) {
      console.log(' User NOT found - Creating new account...');
      const newUser = new UserEntity(
        "", // ID generated by DB
        firstName,
        lastName,
        email.toLowerCase(),
        UserRole.COMPANY_ADMIN,
        "", // Phone
        undefined, // No password hash
        UserStatus.ACTIVE,
        new Date(),
        new Date(),
        undefined, // No companyId yet
        undefined, // No department
        undefined, // No inviteStatus
        false, // hasPassword false for social users initially
        undefined, undefined, undefined, undefined, undefined, undefined, undefined,
        undefined, undefined, undefined, undefined, undefined,
        undefined, undefined, undefined,
        undefined, undefined,
        undefined, undefined,
        [], // skills
        false, // isOnboarded false
        provider,
        providerId
      );

      user = await this._userRepository.create(newUser);
      console.log(' New User Created:', user.id);
    } else {
      console.log(' User Found - ID:', user.id);
      console.log(' Role:', user.role);
      console.log(' IsOnboarded:', user.isOnboarded);
      console.log(' Company ID:', user.companyId);
    }
    console.log('-----------------------------------------');

    if (user.status === UserStatus.SUSPENDED) {
      throw new ForbiddenException(AUTH_MESSAGES.USER_BLOCKED);
    }

    if (user.status !== UserStatus.ACTIVE) {
      throw new ForbiddenException(AUTH_MESSAGES.ACCOUNT_NOT_ACTIVE);
    }

    // Check company suspension ONLY IF onboarded
    if (user.isOnboarded) {
      await this.checkCompanySuspension(user.companyId);
    }

    if (user.role === UserRole.COMPANY_ADMIN && !user.isOnboarded) {
      console.log(' SocialLogin: User NOT onboarded, returning partial data for onboarding redirect');
      return {
        user: {
          id: user.id,
          email: user.email,
          firstName: user.firstName,
          lastName: user.lastName,
          role: user.role,
          isOnboarded: false,
        },
      };
    }

    const accessToken = this._jwtService.generateAccessToken({
      userId: user.id,
      role: user.role,
      companyId: user.companyId,
    });

    const refreshToken = this._jwtService.generateRefreshToken({
      userId: user.id,
    });

    console.log(' SocialLogin: User onboarded, issuing tokens');
    return {
      accessToken,
      refreshToken,
      user: {
        id: user.id,
        email: user.email,
        firstName: user.firstName,
        lastName: user.lastName,
        role: user.role,
        companyId: user.companyId,
        isOnboarded: user.role === UserRole.COMPANY_ADMIN ? user.isOnboarded : true,
      },
    };
  }

  private async checkCompanySuspension(
    companyId: string | undefined,
  ): Promise<void> {
    if (!companyId || !isValidObjectId(companyId)) return;

    const company = await this._companyRepository.findById(companyId);

    if (company?.status === CompanyStatus.SUSPENDED) {
      throw new ForbiddenException(AUTH_MESSAGES.COMPANY_SUSPENDED);
    }
  }
}
